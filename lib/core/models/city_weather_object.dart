class CityWeatherObject {
  Coordinates? coordinates;
  List<Weather>? weather;
  MainWeatherInfo? mainWeatherInfo;
  int? visibility;
  Wind? wind;
  Rain? rain;
  SunDetails? sunDetails;
  String? name;
  int? cod;

  CityWeatherObject(
      {this.coordinates,
      this.weather,
      this.mainWeatherInfo,
      this.visibility,
      this.wind,
      this.rain,
      this.sunDetails,
      this.name,
      this.cod});

  CityWeatherObject.fromJson(Map<String, dynamic> json) {
    coordinates =
        json['coord'] != null ? Coordinates.fromJson(json['coord']) : null;
    if (json['weather'] != null) {
      weather = <Weather>[];
      json['weather'].forEach((v) {
        weather!.add(Weather.fromJson(v));
      });
    }
    mainWeatherInfo =
        json['main'] != null ? MainWeatherInfo.fromJson(json['main']) : null;
    visibility = json['visibility'];
    wind = json['wind'] != null ? Wind.fromJson(json['wind']) : null;
    rain = json['rain'] != null ? Rain.fromJson(json['rain']) : null;

    sunDetails = json['sys'] != null ? SunDetails.fromJson(json['sys']) : null;

    name = json['name'];
    cod = json['cod'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (coordinates != null) {
      data['coord'] = coordinates!.toJson();
    }
    if (weather != null) {
      data['weather'] = weather!.map((v) => v.toJson()).toList();
    }
    if (mainWeatherInfo != null) {
      data['main'] = mainWeatherInfo!.toJson();
    }
    data['visibility'] = visibility;
    if (wind != null) {
      data['wind'] = wind!.toJson();
    }
    if (rain != null) {
      data['rain'] = rain!.toJson();
    }

    if (sunDetails != null) {
      data['sys'] = sunDetails!.toJson();
    }
    data['name'] = name;
    data['cod'] = cod;
    return data;
  }
}

class Coordinates {
  double? lon;
  double? lat;

  Coordinates({this.lon, this.lat});

  Coordinates.fromJson(Map<String, dynamic> json) {
    lon = json['lon'];
    lat = json['lat'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['lon'] = lon;
    data['lat'] = lat;
    return data;
  }
}

